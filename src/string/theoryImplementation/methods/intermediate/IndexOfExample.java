package string.theoryImplementation.methods.intermediate;

public class IndexOfExample {
    public static void main(String[] args) {
        String s = "Programming";
        System.out.println(s.indexOf('g'));     // Output: 3 (first 'g')
        System.out.println(s.lastIndexOf('g')); // Output: 10 (last 'g')
    }
}
